# An example of a simple loop would be

for i in [0, 1, 2, 3, 4, 5]:
    print(i)

# Another way of doing it with less code would be

for y in range(51):
    print(y)

# It could also be:

names = ["Aragorn", "Frodo", "Sam", "Gandalf"]

for name in names:
    print(name)

# And

villain = "Gollum"

for characters in villain:
    print(characters)
